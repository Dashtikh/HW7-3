package com.company;

public class CarpetEnti {

    private int id;
    private String name;
    private int price;


    public int getId() {
        return id;
    }

    public CarpetEnti setid(int id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public CarpetEnti setName(String name) {
        this.name = name;
        return this;
    }

    public int getPrice() {
        return price;
    }

    public CarpetEnti setprice(int price) {
        this.price = price;
        return this;
    }


}
